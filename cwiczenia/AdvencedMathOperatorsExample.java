package cwiczenia;

public class AdvencedMathOperatorsExample {
    public static void main(String[] args) {
        int i = 0;
        i = i + 1;
        i += i;
        i++;
        ++i;
        System.out.println(i);

        int a = 3;
        a++;
        System.out.println(a);
        ++a;
        System.out.println(a);
        System.out.println(++a);
        System.out.println(a++);
        System.out.println(a);
    }
}
